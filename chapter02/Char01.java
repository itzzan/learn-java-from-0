//演示char的基本使用

public class Char01 {
	public static void main(String[] args) {
		char c1 = 'a';
		char c2 = '\t';
		char c3 = '韩';
		char c4 = 97;//说明，字符类型可以直接存放一个数字 - 输出会输出其代表的字符 - 编码的概念
		//修改快捷键 - 首选项 - 按键绑定（默认） - 将要修改的命令整句话复制到按键绑定（用户） - 然后修改保存即可 - 若有多个，上面的要给逗号，最后的不用给
		System.out.println(c1);
		System.out.println(c2);
		System.out.println(c3);
		System.out.println(c4);
	}
}
