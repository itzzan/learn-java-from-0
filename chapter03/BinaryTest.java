//演示四种进制

public class BinaryTest {

	//编写一个main方法
	public static void main(String[] args) {	

		//n1 二进制
		int n1 = 0b1010;
		//n2 十进制
		int n2 = 1010;
		//n3 八进制
		int n3 = 01010;
		//n4 十六进制
		int n4 = 0x10101;
		
		System.out.println("n1=" + n1);
		System.out.println("n2=" + n2);
		System.out.println("n3=" + n3);
		System.out.println("n4=" + n4);
		
	}

}